    private boolean validatePIN(String newPIN, String newPIN2){
        newPIN = txtChangePINNewPIN.getText();
        newPIN2 = txtChangePINNewPINRepeat.getText();
        
        if(isInt(newPIN) && newPIN.length() == 4 && newPIN.equals(newPIN2)){
            return true;            
        }else{
            JOptionPane.showMessageDialog(btnChangePINOK.getRootPane(), "PINs don't match!");
            return false;
        }
    }
    
    private void changePIN(String newPIN){
        ConnectionProvider conn = new ConnectionProvider();
        PanelCards panelCard = new PanelCards();
        int pin = parseStringToInt(newPIN);
        int idCard = panelCard.getCardID();
        conn.changePIN(idCard, pin);
    }

    private boolean isInt(String str){
        try {
            Integer.parseInt(str);
            return true;
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(btnChangePINOK.getRootPane(), "PIN can only be a number 4 digits long!");
            return false;
        }
    }
    
    private int parseStringToInt(String value){
        int intValue = Integer.parseInt(value);
        return intValue;
    }
    
    


